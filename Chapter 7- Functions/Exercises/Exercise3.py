#A short skip of making a t-shirt using def function

def make_shirt(size, message):
    print("")
    print(f"I am going to create a {size} t-shirt.\n")
    print(f'On the t-shirt it will say, "{message}"\n')
make_shirt('small', 'Cute and Handsome')
make_shirt(message="Cute and Handsome", size='medium')